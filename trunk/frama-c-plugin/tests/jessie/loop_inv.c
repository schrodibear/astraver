int i;
int c;

/*@ requires i >=0 && c > 0;
  @ ensures i==0;
  @*/
void f() {
  /*@ loop invariant i >= 0;
    @ loop variant i;
    @*/
  while (i > 0) {
    if (i >= c) {
      i -= c;
    }
    else
      i--;
  }
}

